/* ******************************************************************
 *      Function Name: DT_f_sqrt()
 *
 *      Description: Returns the square root
 *
 *      Arguments: float input	Value to find the square root
 *
 *      Return Value: float
 *
 *      Comments: if value falls with range of data in sqrttable use 
 *				  the array otherwise use sqrt()
 * *****************************************************************/
float DT_f_sqrt(float input)
{	int pos;

	if (input>40000.0f)
	{	return (float)(sqrt(input));	// CAB 08/15/2002	Removed warnings
	}

	if (input<-40000.0f)
	{	return (float)(-sqrt(-input));	// CAB 08/15/2002	Removed warnings
	}
	
	pos=(int)(input);
	if (pos>400)
	{	return (float)(sqrttable[pos/100]*10.0f);	// CAB 08/15/2002	Removed warnings
	}

	if (pos<-400)
	{	return (float)(-sqrttable[-pos/100]*10.0f);	// CAB 08/15/2002	Removed warnings
	}

	pos=(int)(input);
	if (pos<0)
	{	return(-sqrttable[-pos]);
	}
	return(sqrttable[pos]);
}
